// Define critical indicators for follow-up assessment
// These are questions where a "No" answer is concerning (or "Yes" for questions 2, 5, 12)
export const criticalQuestions = [2, 5, 7, 9, 10, 12, 14, 16, 19];

// Simple risk assessment based on M-CHAT-R guidelines
export const assessRisk = (answers: Record<number, boolean>): { level: number, description: string } => {
  // Count total "at risk" answers
  let atRiskCount = 0;
  let criticalAtRiskCount = 0;
  
  for (const [questionId, answer] of Object.entries(answers)) {
    const qId = parseInt(questionId);
    
    // For questions 2, 5, 12, "Yes" is concerning
    if ((qId === 2 || qId === 5 || qId === 12) && answer === true) {
      atRiskCount++;
      if (criticalQuestions.includes(qId)) {
        criticalAtRiskCount++;
      }
    } 
    // For all other questions, "No" is concerning
    else if ((qId !== 2 && qId !== 5 && qId !== 12) && answer === false) {
      atRiskCount++;
      if (criticalQuestions.includes(qId)) {
        criticalAtRiskCount++;
      }
    }
  }
  
  // Determine risk level based on total score and critical items
  if (atRiskCount >= 8 || criticalAtRiskCount >= 3) {
    return { level: 3, description: "High Risk" };
  } else if (atRiskCount >= 3 || criticalAtRiskCount >= 2) {
    return { level: 2, description: "Medium Risk" };
  } else if (atRiskCount >= 1) {
    return { level: 1, description: "Low Risk" };
  } else {
    return { level: 0, description: "Minimal Risk" };
  }
};
